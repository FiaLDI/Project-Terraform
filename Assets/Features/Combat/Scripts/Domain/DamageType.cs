namespace Features.Combat.Domain
{
    /// <summary>
    /// Типы урона в игре.
    /// Используется CombatService для расчёта резистов, эффектов и поведения попаданий.
    /// </summary>
    public enum DamageType
    {
        /// <summary>
        /// Базовый урон без свойств.
        /// </summary>
        Generic,

        /// <summary>
        /// Пули, стрелы и любое оружие дальнего боя.
        /// Может учитываться броней, падением урона, пробитием.
        /// </summary>
        Ballistic,

        /// <summary>
        /// Радиальный или точечный урон от взрывов, гранат, мин.
        /// Может иметь falloff по дистанции.
        /// </summary>
        Explosive,

        /// <summary>
        /// Огненный урон — может накладывать DoT или “burning” эффект.
        /// </summary>
        Fire,

        /// <summary>
        /// Урон от инструментов добычи (дробилок, дрелей).
        /// Может игнорировать броню, наносить повышенный урон ресурсам.
        /// </summary>
        Mining,

        /// <summary>
        /// Ближний бой — мечи, топоры, кувалды.
        /// Может иметь повышенный stagger или bleeding.
        /// </summary>
        Melee,

        /// <summary>
        /// Электрический удар — оглушение, шок, отключение электроники/щитков.
        /// </summary>
        Electric,

        /// <summary>
        /// Ядовитый — яд, токсичные облака, споры.
        /// Всегда накладывает DoT.
        /// </summary>
        Poison,

        /// <summary>
        /// Холод — может замедлять или снижать эффективность врагов.
        /// </summary>
        Frost,

        /// <summary>
        /// Кислота — повреждает броню, корродирует.
        /// </summary>
        Acid
    }
}
